
package com.github.fotohh.gui;
/**
 * The GUIType enum represents different types of GUIs that can be created using the GUIManager class.
 * It defines the available GUI types: ANVIL, CHEST, WORKBENCH, and CREATIVE.
 */
public enum GUIType {

    /**
     * Represents an Anvil GUI type.
     */
    ANVIL,

    /**
     * Represents a Chest GUI type.
     */
    CHEST,

    /**
     * Represents a Workbench GUI type.
     */
    WORKBENCH,
}